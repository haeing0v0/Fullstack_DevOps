package inher;

public class Operation {
	//메서드 작성 : double sum(double x, double y), double sub(double x, double y),
	//double mul(double x, double y), double div(double x, double y)
	
	int sum(int x, int y) {
		return x + y;
	}
	
	int sub(int x, int y) {
		return x - y;
	}
	
	int mul(int x, int y) {
		return x * y;
	}
	
	int div(int x, int y) {
		return x / y;
	}
}
