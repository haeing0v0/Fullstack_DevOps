package method;

public class Operation {
	//메서드 작성 : double sum(double x, double y), double sub(double x, double y),
	//double mul(double x, double y), double div(double x, double y)
	
	double sum(double x, double y) {
		return x + y;
	}
	
	double sub(double x, double y) {
		return x - y;
	}
	
	double mul(double x, double y) {
		return x * y;
	}
	
	double div(double x, double y) {
		return x / y;
	}
}
